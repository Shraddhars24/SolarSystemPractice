public class Planet2 {
    String Moon;
    String Sun;
    String Planets;
    String Stars;

    public Planet2(String moon, String sun, String planets, String stars) {
        this.Moon = moon;
        this.Sun = sun;
        this.Planets = planets;
        this.Stars = stars;
    }

    public String getMoon() {
        return Moon;
    }

    public String getSun() {
        return Sun;
    }
    public String getPlanets() {
        return Planets;
    }
    public String getStars() {
        return Stars;
    }






}
